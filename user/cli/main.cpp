// simtemp/user/cli/main.cpp
#include <iostream>
#include <string>


/****************************************************************************/
/******************************** PRINT HELP ********************************/
/****************************************************************************/

static void printHelp()
{
   std::cout << "simtempCLI (hello)\n";
   std::cout << "Usage:\n";
   std::cout << "  ./simtempCLI               # Hello world\n";
   std::cout << "  ./simtempCLI -help        # Show this help screen\n";
}

/****************************************************************************/
/****************************************************************************/
/********************************  M A I N  *********************************/
/****************************************************************************/
/****************************************************************************/


int main(int argc, char** argv)
{
   if (argc > 1)
   {
      std::string arg = argv[1];
      if (arg == "-help" || arg == "-h")
      {
         printHelp();
         return 0;
      }
   }

   std::cout << "simtemp CLI: Hello world!\n";
   return 0;
}
